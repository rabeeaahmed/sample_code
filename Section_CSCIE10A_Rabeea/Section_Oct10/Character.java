public class Character { 
   public static void main(String[] args) { 
   		/** When we put something in inverted commas, then 
   		that something is printed literally to the screen. 
   	
   		If we don't have inverted commas, then the variable value
   		that that something holds is printed **/
   		
		for(int i = 0; i < 10; i += 1){
			System.out.println("i");
			System.out.println(i);
			System.out.println(i+i);
			System.out.println("i + i");
		}
   }
}